package com.polus.tvaddtool.report.vo;

public class ReportResponseVO {

	private String programName;

	private String visitedURL;

	private String timeZone;

	private String visitedTime;

	private String broadCastTime;

	public String getProgramName() {
		return programName;
	}

	public void setProgramName(String programName) {
		this.programName = programName;
	}

	public String getVisitedURL() {
		return visitedURL;
	}

	public void setVisitedURL(String visitedURL) {
		this.visitedURL = visitedURL;
	}

	public String getTimeZone() {
		return timeZone;
	}

	public void setTimeZone(String timeZone) {
		this.timeZone = timeZone;
	}

	public String getVisitedTime() {
		return visitedTime;
	}

	public void setVisitedTime(String visitedTime) {
		this.visitedTime = visitedTime;
	}

	public String getBroadCastTime() {
		return broadCastTime;
	}

	public void setBroadCastTime(String broadCastTime) {
		this.broadCastTime = broadCastTime;
	}

}
